MD5_context *Curl_MD5_init(const MD5_params *md5params)
{
  MD5_context *ctxt;


  ctxt = Curl_cmalloc(sizeof *ctxt);

  if(!ctxt)
    return ctxt;

  ctxt->md5_hashctx = Curl_cmalloc(md5params->md5_ctxtsize);

  if(!ctxt->md5_hashctx) {
    Curl_cfree(ctxt);
    return ((void*)0);
  }

  ctxt->md5_hash = md5params;

  (*md5params->md5_init_func)(ctxt->md5_hashctx);

  return ctxt;
}

